num = int(input("Enter a Number: "))

# Conditional Statements checks that if the number divided by 2 gives 0 as reminder(Even).
if num % 2 == 0:
    print(f"The Number {num} is an Even Number")  # Prints out if the Number is Even

else:
    print(f"The Number {num} is a Odd Number") # If the above Condition is False, Prints out the number is Odd
